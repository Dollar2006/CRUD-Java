    private void atualizaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_atualizaActionPerformed
        Cliente cliente = new Cliente();
        cliente.setNome(jTextField1.getText().trim());
        cliente.setCpf(jTextField2.getText().trim());
        cliente.setEmail(jTextField3.getText().trim());
        cliente.setTel(jTextField4.getText().trim());
        cliente.setEndereco(jTextField5.getText().trim());
        cliente.setData_nasc(jTextField6.getText().trim());

  
        String dataNascimentoStr = cliente.getData_nasc();
        if (dataNascimentoStr != null && !dataNascimentoStr.isEmpty()) {
            Pattern pattern = Pattern.compile("^\\d{4}-\\d{2}-\\d{2}$");
            Matcher matcher = pattern.matcher(dataNascimentoStr);
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(null, "Data de nascimento inválida. Formato esperado: AAAA-MM-DD");
                return;
            }
        }


        if (cliente.getCpf().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o CPF para atualizar o cliente.");
        } else {
            ClienteDAO dao = new ClienteDAO();
            dao.atualiza(cliente);  // Chama o método de update
            JOptionPane.showMessageDialog(null, "Cliente atualizado com sucesso!");
        }
    }//GEN-LAST:event_atualizaActionPerformed

    private void removerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removerActionPerformed
        String cpfParaExcluir = jTextField2.getText().trim();

        if (cpfParaExcluir.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Informe o CPF do cliente que deseja excluir.");
        } else {
            int confirmacao = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja excluir o cliente com CPF: " + cpfParaExcluir + "?", "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);

            if (confirmacao == JOptionPane.YES_OPTION) {
                ClienteDAO dao = new ClienteDAO();
                dao.deleta(cpfParaExcluir);
                JOptionPane.showMessageDialog(null, "Cliente excluído com sucesso!");
            }
        }
    }//GEN-LAST:event_removerActionPerformed

