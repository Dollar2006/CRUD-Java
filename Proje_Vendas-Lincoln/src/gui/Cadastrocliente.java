    private void cadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cadastrarActionPerformed
        CPFValidator validator = new CPFValidator();
        Cliente cliente = new Cliente();
        cliente.setNome(jTextField1.getText());
        cliente.setCpf(jTextField2.getText());
        cliente.setEmail(jTextField3.getText());
        cliente.setTel(jTextField4.getText());
        cliente.setEndereco(jTextField5.getText());
        cliente.setData_nasc(jTextField6.getText());
        
        
         try {
            validator.assertValid(jTextField2.getText());
            // Exibe uma mensagem de sucesso
            JOptionPane.showMessageDialog(null, "-> " + jTextField2.getText() + " é VÁLIDO (assertValid não lançou exceção).", "Validação bem-sucedida", JOptionPane.INFORMATION_MESSAGE);
        } catch (InvalidStateException e) {
            // Este bloco não deve ser executado para um CPF válido que passe no algoritmo
            // Exibe uma mensagem de erro
            JOptionPane.showMessageDialog(null, "-> " + jTextField2.getText() + " é INVÁLIDO: " + e.getInvalidMessages(), "Erro de Validação", JOptionPane.ERROR_MESSAGE);
            return;
        }


        // Validação da data de nascimento (AAAA-MM-DD)
        String dataNascimentoStr = cliente.getData_nasc();
        if (dataNascimentoStr != null && !dataNascimentoStr.isEmpty()) {
            Pattern pattern = Pattern.compile("^\\d{4}-\\d{2}-\\d{2}$");
            Matcher matcher = pattern.matcher(dataNascimentoStr);
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(null, "Data de nascimento inválida. Formato esperado: AAAA-MM-DD");
                return; // Impede a inserção se a data for inválida
            }
        }

        String emailStr = cliente.getEmail();

        if (emailStr != null && !emailStr.isEmpty()) {
            Pattern pattern = Pattern.compile("^.+@.+\\.com$");
            Matcher matcher = pattern.matcher(emailStr);
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(null, "O email está escrito de forma inválida. Formato esperado: NomeUsuário@email.com");
                return;
            }
        }

        String cpfStr = cliente.getCpf();

        if (cpfStr != null && !cpfStr.isEmpty()) {
            Pattern pattern = Pattern.compile("^\\d{3}\\.\\d{3}\\.\\d{3}-\\d{2}$");
            Matcher matcher = pattern.matcher(cpfStr);
            if (!matcher.matches()) {
                JOptionPane.showMessageDialog(null, "CPF inválido. Formato esperado: XXX.XXX.XXX-XX");
                return;
            }
            // CPF está no formato correto
        }
        
  

        // Validação de campos obrigatórios
        if (cliente.getNome().isEmpty() || cliente.getCpf().isEmpty() || cliente.getEmail().isEmpty() || cliente.getTel().isEmpty() || cliente.getEndereco().isEmpty()) {
            JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios.");
        } else {
            ClienteDAO dao = new ClienteDAO();
            try {
                dao.adiciona(cliente);
            } catch (SQLException ex) {
                Logger.getLogger(Cadastrocliente.class.getName()).log(Level.SEVERE, null, ex);
            }
                
        }
    }//GEN-LAST:event_cadastrarActionPerformed

